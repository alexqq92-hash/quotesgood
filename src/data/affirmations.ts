// Shuffle function to randomize affirmations
export function shuffleAffirmations(arr: Affirmation[]): Affirmation[] {
  const shuffled = [...arr];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
}

// Get affirmations by category
export function getAffirmationsByCategory(category: AffirmationCategory): Affirmation[] {
  return affirmations.filter((a: Affirmation) => a.category === category);
}

// Get affirmations by multiple categories
export function getAffirmationsByCategories(categories: AffirmationCategory[]): Affirmation[] {
  return affirmations.filter((a: Affirmation) => categories.includes(a.category));
}

